from langchain_openai import ChatOpenAI
from langchain.prompts.chat import ChatPromptTemplate
from dotenv import load_dotenv
import os

load_dotenv()

api_key = os.getenv("OPENAI_API_KEY")

chat_model = ChatOpenAI(openai_api_key= api_key)

Talk = input("What do you want to translate?\n\n")

Lanish = int(input("\n1:Spanish \n2:French \n3:Chinese\n Press number to choose language: ") or 0)

if Lanish == 1:
    Pref = ("Spanish")
elif Lanish == 2:
    Pref = ("French")
elif Lanish == 3:
    Pref = ("Chinese")
elif Lanish == 0:
    Pref = ("Eritrean")




template = "You are a helpful assistant {input_language} to {output_language}."
human_template = "{text}"

chat_prompt = ChatPromptTemplate.from_messages([
    ("system", template),
    ("human", human_template),
])

messages = chat_prompt.format_messages(input_language="English",
                                       output_language=Pref,
                                       text=Talk)

result = chat_model.invoke(messages)

print(result.content)































































































































































































































































































































































































































































































































































































































































































